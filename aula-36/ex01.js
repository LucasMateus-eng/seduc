// Acessando propriedades de um objeto

let pessoa = {
	nome: "Fulano",
	idade: 30,
	cidade: "São Paulo",
};

// Acessando propriedades usando notação de ponto
console.log(pessoa.nome); // "Fulano"
console.log(pessoa.idade); // 30

// Acessando propriedades usando colchetes
console.log(pessoa["cidade"]); // "São Paulo"
